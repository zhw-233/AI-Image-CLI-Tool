import { readFile, writeFile } from "node:fs/promises";
import { existsSync } from "node:fs";
import path from "node:path";
import process from "node:process";

const CONFIG_FILE = "providers.json";
const EXAMPLE_CONFIG_FILE = "providers.example.json";
const ACTIVE_ENV_FILE = ".env.active";

async function main() {
  const [command = "list", profileKey] = process.argv.slice(2);

  if (command === "help" || command === "--help" || command === "-h") {
    printHelp();
    return;
  }

  if (command === "list") {
    await listProviders();
    return;
  }

  if (command === "current") {
    await printCurrent();
    return;
  }

  if (command === "use") {
    if (!profileKey) {
      fail("Missing provider name. Example: npm run provider:use -- third-party");
    }

    await useProvider(profileKey);
    return;
  }

  fail(`Unknown command: ${command}`);
}

async function listProviders() {
  const config = await readProviderConfig({ allowExample: true });

  console.log("Available providers:");
  for (const [key, provider] of Object.entries(config.providers)) {
    const label = provider.name ? `${key} (${provider.name})` : key;
    console.log(`- ${label}: ${provider.baseUrl}`);
  }

  if (!existsSync(path.resolve(CONFIG_FILE))) {
    console.log(`\nCreate ${CONFIG_FILE} from ${EXAMPLE_CONFIG_FILE} before switching.`);
  }
}

async function printCurrent() {
  const activePath = path.resolve(ACTIVE_ENV_FILE);
  if (!existsSync(activePath)) {
    console.log("No active provider. Run: npm run provider:use -- <name>");
    return;
  }

  const active = await readFile(activePath, "utf8");
  console.log(active.trim());
}

async function useProvider(profileKey) {
  const config = await readProviderConfig({ allowExample: false });
  const provider = config.providers?.[profileKey];
  if (!provider) {
    fail(`Provider "${profileKey}" was not found in ${CONFIG_FILE}.`);
  }

  validateProvider(profileKey, provider);

  const contents = renderActiveEnv(profileKey, provider);
  await writeFile(ACTIVE_ENV_FILE, contents);
  console.log(`Activated provider "${profileKey}" in ${ACTIVE_ENV_FILE}`);
}

async function readProviderConfig({ allowExample }) {
  const configPath = path.resolve(CONFIG_FILE);
  const fallbackPath = path.resolve(EXAMPLE_CONFIG_FILE);
  const filePath = existsSync(configPath) ? configPath : fallbackPath;

  if (!existsSync(filePath) || (!existsSync(configPath) && !allowExample)) {
    fail(`Create ${CONFIG_FILE} from ${EXAMPLE_CONFIG_FILE}, then edit it for your provider.`);
  }

  const contents = await readFile(filePath, "utf8");
  const config = JSON.parse(contents);
  if (!config.providers || typeof config.providers !== "object") {
    fail(`${path.basename(filePath)} must contain a "providers" object.`);
  }

  return config;
}

function validateProvider(profileKey, provider) {
  if (!provider.baseUrl && !provider.apiUrl) {
    fail(`Provider "${profileKey}" needs baseUrl or apiUrl.`);
  }

  if (!provider.model) {
    fail(`Provider "${profileKey}" needs model.`);
  }
}

function renderActiveEnv(profileKey, provider) {
  const lines = [
    "# Generated by src/switch-provider.js. Do not edit by hand.",
    `IMAGE_PROVIDER=${profileKey}`,
  ];

  append(lines, "OPENAI_BASE_URL", provider.baseUrl);
  append(lines, "OPENAI_IMAGE_GENERATIONS_URL", provider.apiUrl);
  append(lines, "OPENAI_IMAGE_MODEL", provider.model);
  append(lines, "OPENAI_API_KEY_ENV", provider.apiKeyEnv);
  append(lines, "OPENAI_API_KEY", provider.apiKey);
  append(lines, "OPENAI_AUTH_HEADER", provider.authHeader);
  append(lines, "OPENAI_AUTH_SCHEME", provider.authScheme);

  if (provider.extraHeaders) {
    lines.push(`OPENAI_EXTRA_HEADERS=${JSON.stringify(provider.extraHeaders)}`);
  }

  return `${lines.join("\n")}\n`;
}

function append(lines, key, value) {
  if (value === undefined || value === null) {
    return;
  }

  lines.push(`${key}=${escapeEnvValue(String(value))}`);
}

function escapeEnvValue(value) {
  if (/[\s#"']/u.test(value)) {
    return JSON.stringify(value);
  }

  return value;
}

function printHelp() {
  console.log(`
Usage:
  npm run providers
  npm run provider:current
  npm run provider:use -- <provider>

`);
}

function fail(message) {
  console.error(message);
  process.exit(1);
}

main().catch((error) => fail(error.stack ?? error.message));
